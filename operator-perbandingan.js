
/*
	 operator pembandingan 
		== -> sama dengan
		=== -> sama dengan dan sama tipe

		!= -> tidak sama dengan
		!== -> tidak sama dengan atau tidak sama tipe
*/

let stok1 = 5 == "5";
console.log(stok1);

let stok2 = 5 === "5";
console.log(stok2);

let barang1 = 10 != 3;
console.log(barang1);

let barang2 = 10 !== "10";
console.log(barang2);